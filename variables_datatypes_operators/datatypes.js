// Strings are used to store and manipulate text.
let str = "hello"; 
str = 10; 
console.log(typeof str); // Output: string

// Numbers are used to store and manipulate numeric values.
let num = 10;
console.log(typeof num); // Output: number

// Booleans are used to store and manipulate true/false values.
let bool = true;
console.log(typeof bool); // Output: boolean

// Null is used to represent a deliberate non-value.
let n = null;
console.log(typeof n); // Output: object (this is a quirk in JavaScript)

// Undefined is used to represent an uninitialized value.
let u = undefined;
console.log(typeof u); // Output: undefined

// Symbols are used to create unique identifiers.
let sym = Symbol("mySymbol");
console.log(typeof sym); // Output: symbol
console.log(sym); // Output: symbol
