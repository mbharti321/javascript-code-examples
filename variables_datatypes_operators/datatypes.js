// Strings are used to store and manipulate text.
let str = "hello";
console.log(str); // Output: hello

// Numbers are used to store and manipulate numeric values.
let num = 10;
console.log(num); // Output: 10

// Booleans are used to store and manipulate true/false values.
let bool = true;
console.log(bool); // Output: true

// Null is used to represent a deliberate non-value.
let n = null;
console.log(n); // Output: null

// Undefined is used to represent an uninitialized value.
let u = undefined;
console.log(u); // Output: undefined

// Symbols are used to create unique identifiers.
let sym = Symbol("mySymbol");
console.log(sym); // Output: Symbol(mySymbol)
