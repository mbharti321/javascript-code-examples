// Arithmetic operators are used to perform mathematical operations on numbers.
let a = 5;
let b = 2;
console.log(a + b); // Output: 7
console.log(a - b); // Output: 3
console.log(a * b); // Output: 10
console.log(a / b); // Output: 2.5
console.log(a % b); // Output: 1

// Assignment operators are used to assign values to variables.
let c = 10;
// c = c+5
c += 5;
console.log(c); // Output: 15
console.log("~~~~~~~~~~"); 

// Comparison operators are used to compare values and return a boolean result.
let d = 7;
let e = 4;
console.log(d > e); // Output: true
console.log(d < e); // Output: false
console.log(d >= e); // Output: true
console.log(d <= e); // Output: false
console.log(d == e); // Output: false
console.log(d != e); // Output: true
console.log("~~~~~~~~~~"); 

console.log(10 == "10"); // Output: true
console.log(10 === "10"); // Output: false
console.log(10 !== "10"); // Output: true

console.log("~~~~~~~~~~"); 
// Logical operators are used to perform logical operations on boolean values.
let f = true;
let g = false;
console.log(f && g); // Output: false
console.log(f || g); // Output: true
console.log(!g); // Output: true
